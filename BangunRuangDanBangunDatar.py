import math

#fungsi keliling balok
def kelilingBalok(p, l, t):
    return 4 * (p + l + t)

#fungsi volume balok
def volumeBalok (p, l, t):
    return p * l * t

#fungsi luas jajar genjang
def luasJajarGenjang(a, t):
    return a * t

#fungsi keliling jajar genjang
def kelilingJajarGenjang(s):
    return s + s + s + s

#fungsi keliling kubus
def kelilingKubus(s):
    return 12 * s

#fungsi volume kubus
def volumeKubus(s):
    return s * s * s


